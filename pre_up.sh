#!/opt/bin/bash
#    v 0.0.1, c plgroves gmail 2022
#    SCRIPTNAME 
#        runs before pia-wireguard service is up
#        sets a safe and sane environment
#         e.g. dns, port forwarding and transmission
#        
####
# 
    _Usage() {
         sed >&2 -n "1d; /^###/q; /^#/!q; s/^#*//; s/^ //; 
                     s/SCRIPTNAME/${BASH_SOURCE##*/}/; p" \
                    "${BASH_SOURCE%/*}/${BASH_SOURCE##*/}"
         exit 1; }
    [[ "$1" =~ ^[-hH] ]] && _Usage "$@"

    function logger() {
        local message="${1}"; local source="${2:-${BASH_SOURCE}}"; local log="${3:-$LOG}"
        local tab spaces 
        tab="${TAB:-100}"
        IFS="" spaces="$(printf "%$((tab*2))s")"
        printf %s:[%s]:%.$((${tab}-${#source}))s%s%s  "$(date)" "$(cut -d- -f2- <<< "${source##*/}") " "${spaces} " "${message}" $'\n'| tee -a "${log}"
}

    log='/dev/null'
    LOG="${1:-${log}}"
    bash_source="${#BASH_SOURCE}"; export TAB=$((bash_source+1))

  # PIA's scripts are set to a relative path
    cd "${0%/*}" || exit 255

    export PATH=/opt/bin:/opt/sbin:/usr/bin:/usr/sbin
#logger "This script starts applications after connection"
#exit 0

  # stop any VPN's previously started
    logger "Stopping any existing VPN"
    connmanctl disconnect "$(grep VPN < <( connmanctl services) | awk '{print $NF}')"
      
  # clear firewall
    logger "Clearing firewall"
    iptables-restore < openrules.v4 

  # wait for internet
    logger ""
    until ping -c 1  -W 1  google.com > /dev/null 2>&1
    do sleep 2
       # having problems with my nameserver?
       # restore a sane DNS
    cat <<-EOF > /etc/resolv.conf
    # Generated by PIA WIREGUARD
    nameserver 8.8.8.8
    namesever 1.1.1.1
	EOF
    done

  # stop transmission from running in the wild
    if grep -q alive < <(/opt/etc/init.d/S88transmission check)
    then logger "Stopping transmission daemon"
         /opt/etc/init.d/S88transmission stop >/dev/null
    fi

  # stop portforwarding 
    logger "Stopping port forwarding"
    ps aux|grep '[p]f\.' | awk '{print $2}' | xargs kill >/dev/null 2>&1

exit 0
